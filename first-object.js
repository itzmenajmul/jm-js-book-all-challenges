/*
তোর আশেপাশে কোনো প্রাণী থাকলে (গরু, বিড়াল, কুকুর, ইঁদুর, তেলাপোকা, মশা, মাছি, পিঁপড়া, টিকটিকি, মানুষ বা অন্য কোনো প্রাণী থাকলে), সেটার জন্য একটা অবজেক্ট লিখে ফেল।
 */
let goru = {
    color: "red",
    age: 3,
    weight: 120,
    teeth: 24,
    name: "Buruk"
}
console.log(goru);

/*
তোর আব্বুর জন্য father নামে একটা অবজেক্ট বানিয়ে ফেল। সেখানে উনার নাম, পেশা, বয়স, সন্তানের সংখ্যা ইত্যাদি তথ্য দিয়ে ফেল।
 */
let father = {
    name: "Hulu",
    job: "Unemploye",
    age: 55,
    children: 4,
    isAngry : true
}

