/*
তুই 5টা পছন্দের সাবজেক্টের নাম লেখ। তারপর প্রোগ্রাম লিখে দেখ, কীভাবে লুপ ব্যবহার করে সব সাবজেক্টের নাম আলাদাভাবে প্রিন্ট হবে। */
let favSubjects = ["Math", "English", "ICT", "History", "Science"];

for(const sub of favSubjects){
    console.log(sub);
}

/*
তোর একদম পছন্দের খাবার গুলোর নাম লেখ। এখন লুপ ব্যবহার করে প্রোগ্রাম লিখে দেখ, কীভাবে সব খাবারের নাম আলাদা আলাদা করে প্রিন্ট হবে।
 */
let myFavFood = ["Briyani", "Kachi", "Burger", "Doi", "Dal Bath"];
for(const food of myFavFood){
    console.log(food);
}

/*
একটা অ্যারের মধ্যে তোর ফ্যামিলি মেম্বারদের জন্মসাল লিখে ফেল। তারপর লুপ চালিয়ে সবার জন্মসাল আউটপুট হিসেবে দেখা।
 */
let familyMembersDateOfYear = [2000, 2003, 2013, 2014, 2024 ];
for(const year of familyMembersDateOfYear){
    console.log(year);
}

/*
ক্রিকেট বা ফুটবল খেলায় তোর প্রিয় একাদশের একটা টিম বানিয়ে তোর প্রিয় খেলোয়াড়দের নাম একটা অ্যারের মধ্যে রাখ। তারপর কনসোল লগ করে সব খেলোয়াড়দের নাম আউটপুট হিসেবে দেখবি। 
 */
let priyoEkadosh = ["Nur", "Salar", "Tom", "Yamin", "Summu", "Tusho", "Bodo", "Rod", "Yelup", "Don", "Bito"];
for(const player of priyoEkadosh){
    console.log(player);
}

/*নেক্সট কবে কবে তোর পরীক্ষা আছে, সেই তারিখগুলা অ্যারের মধ্যে লিখে ফেল। তারপর লুপ ব্যবহার করে পরীক্ষার তারিখগুলা কনসোল লগ কর।
 */
