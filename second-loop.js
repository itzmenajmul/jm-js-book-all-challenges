// JavaScript While Practice:

/*
while লুপ দিয়ে 10 বার কনসোল লগ কর। আমি প্রতিদিন মিনিমাম 3 ঘণ্টা করে প্রোগ্রামিং প্র্যাকটিস করব। 
 */
let hour = 0;
while(hour < 10){
    console.log("Ami protidin 3 gonta kore programming practice korbo");
    hour++;
}

/*
তুই 1 থেকে 20 পর্যন্ত সংখ্যা লিখবি একটা while লুপ দিয়ে।
 */
let randomNums = 1;
while(randomNums <= 20){
    console.log(randomNums);
    randomNums++;
}

/*
while লুপ দিয়ে 50 থেকে 100 পর্যন্ত সংখ্যা প্রিন্ট কর।
 */
let fiftyToHundred = 50;
while(fiftyToHundred <= 100){
    console.log(fiftyToHundred);
    fiftyToHundred++;
}

/*
একটি while লুপ দিয়ে 1 থেকে 10 পর্যন্ত সংখ্যাগুলো প্রিন্ট করার সময় সংখ্যাগুলাকে 3 দিয়ে গুণ করে ফেলবি। তাহলে কিন্তু 3-এর নামটা হয়ে যাবে। 
 */
let tinErNamota = 1;
while(tinErNamota <= 10){
    console.log(tinErNamota * 3);
    tinErNamota++;
}

